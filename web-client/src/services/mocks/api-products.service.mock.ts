import { IBrand, IProduct } from '../../shared/interfaces/products.interface';
import { rest } from 'msw';
import { API_PRODUCTS_BASE_URL } from '../../shared/constants/services.const';
import { getPagination } from '../../lib/mock-helper';

const baseUrl = API_PRODUCTS_BASE_URL;

export const handlers = [
  rest.get(`${baseUrl}`, (req, res, ctx) => {
    const { perPage, offset } = getPagination(req);
    const products = MOCK_PRODUCTS.slice(offset, offset! + Number(perPage));
    return res(ctx.json(products));
  }),
  rest.get(`${baseUrl}/brands`, (req, res, ctx) => {
    return res(ctx.json(MOCK_PRODUCTS_BRANDS));
  }),
  rest.get(`${baseUrl}/search/count`, (req, res, ctx) => {
    const keywords = req.url.searchParams.get('keywords');
    const count = MOCK_PRODUCTS.filter(
      (product) => product.brand.toLowerCase() === keywords?.toLowerCase(),
    ).length;
    return res(ctx.json(count));
  }),
  rest.get(`${baseUrl}/count`, (req, res, ctx) => {
    return res(ctx.json(MOCK_PRODUCTS.length));
  }),
  rest.get(`${baseUrl}/search`, (req, res, ctx) => {
    const { perPage, offset } = getPagination(req);
    const keywords = req.url.searchParams.get('keywords');
    const products = MOCK_PRODUCTS.filter(
      (product) => product.brand.toLowerCase() === keywords?.toLowerCase(),
    ).slice(offset, offset! + Number(perPage));
    return res(ctx.json(products));
  }),
  rest.get(`${baseUrl}/:id`, (req, res, ctx) => {
    const { id } = req.params;
    const product = MOCK_PRODUCTS_WITH_SOURCES.find(
      (product) => product.id === id,
    );
    return res(ctx.status(200), ctx.json({ ...product }));
  }),
];

